import streamlit as st
import os, sys
sys.path.insert(0, os.path.dirname(os.path.dirname(__file__)))
from utils.loader import load_insights, get_report_path, report_exists


def _chart_card(plot_key: str, title: str, insight_html: str):
    fname  = f"{plot_key}.png" if not plot_key.endswith(".png") else plot_key
    st.markdown(f'<div class="af-card"><div class="af-card-title">{title}</div>',
                unsafe_allow_html=True)
    if report_exists(fname):
        st.image(get_report_path(fname), use_container_width=True)
    else:
        st.info("Run `cd ml_pipeline && python train.py` to generate this chart.", icon="📊")
    if insight_html:
        st.markdown(f'<div class="af-insight">{insight_html}</div>',
                    unsafe_allow_html=True)
    st.markdown('</div>', unsafe_allow_html=True)


def render():
    ins = load_insights()

    st.markdown("""<div class="af-page-hdr">
      <h2>📊  EDA &amp; Visual Insights</h2>
      <p>16 plots auto-generated by the ML pipeline · Text insights from
         <code>reports/insights.json</code> · Refreshes on every training run</p>
    </div>""", unsafe_allow_html=True)

    if not ins:
        st.warning(
            "No EDA data found. Run `cd ml_pipeline && python train.py` first.",
            icon="⚙️"
        )
        return

    # ── Dataset summary strip ──────────────────────────────────────────────────
    st.markdown(f"""
    <div class="af-kpi-strip">
      <div class="af-kpi">
        <div class="af-kpi-value">{ins.get('total_rows',0):,}</div>
        <div class="af-kpi-label">Total Records</div>
      </div>
      <div class="af-kpi">
        <div class="af-kpi-value gold">₹{ins.get('price_min',0):,}</div>
        <div class="af-kpi-label">Min Price</div>
      </div>
      <div class="af-kpi">
        <div class="af-kpi-value gold">₹{ins.get('price_max',0):,}</div>
        <div class="af-kpi-label">Max Price</div>
      </div>
      <div class="af-kpi">
        <div class="af-kpi-value">₹{ins.get('price_mean',0):,}</div>
        <div class="af-kpi-label">Mean Price</div>
      </div>
      <div class="af-kpi">
        <div class="af-kpi-value">₹{ins.get('price_median',0):,}</div>
        <div class="af-kpi-label">Median Price</div>
      </div>
      <div class="af-kpi">
        <div class="af-kpi-value">₹{ins.get('price_std',0):,}</div>
        <div class="af-kpi-label">Std Dev</div>
      </div>
    </div>""", unsafe_allow_html=True)

    tabs = st.tabs(["💰 Price","💺 Class","✈ Airlines",
                    "📐 Distance","📅 Season","🛑 Stops",
                    "🌿 BRD Factors","🔗 Correlations","📈 Advanced"])

    # ── Price ──────────────────────────────────────────────────────────────────
    with tabs[0]:
        _chart_card("01_price_distribution",
            "Price Distribution — Raw · Log-transformed · By Class",
            f"""<strong>Key insights:</strong><br>
            · Price range ₹{ins.get('price_min',0):,} – ₹{ins.get('price_max',0):,}<br>
            · Mean ₹{ins.get('price_mean',0):,} &gt; Median ₹{ins.get('price_median',0):,}
              — right-skewed due to premium cabin outliers<br>
            · Log-transform produces a near-normal distribution,
              essential for the linear baseline model<br>
            · Boxplot confirms First / Business have the widest IQR —
              high route-airline variance at premium tiers""")

        _chart_card("15_outlier_analysis",
            "Outlier Analysis — Z-score flag · IQR by Class",
            """<strong>Key insights:</strong><br>
            · Z-score &gt; 3 flags ultra-long-haul First class routes as outliers<br>
            · First class IQR is 3× wider than Economy — premium pricing
              is far more variable by route and airline<br>
            · Outliers are <em>retained</em> — they are genuine price points,
              not data errors, and help the model learn extreme cases""")

    # ── Class ──────────────────────────────────────────────────────────────────
    with tabs[1]:
        eco = ins.get("economy_mean",0); first = ins.get("first_mean",0)
        mult = ins.get("first_vs_economy","—")
        _chart_card("02_price_by_class",
            "Cabin Class — Mean ± Std Dev · Volume Split",
            f"""<strong>Key insights:</strong><br>
            · First class (₹{first:,}) costs <strong>{mult}×</strong>
              Economy (₹{eco:,}) on average<br>
            · Economy dominates bookings (~52%) — matching real-world distribution<br>
            · Cabin Class is the <strong>#1 price driver</strong> in the model""")

        _chart_card("12_class_distance_heatmap",
            "Price Heatmap — Cabin Class × Distance Band",
            """<strong>Key insights:</strong><br>
            · First class commands a premium at <em>every</em> distance band<br>
            · Short-haul Business often costs more than long-haul Economy —
              cabin dominates route<br>
            · Class × Distance interaction feature ranks top-3 in the model""")

    # ── Airlines ───────────────────────────────────────────────────────────────
    with tabs[2]:
        top_a = ins.get("top_airline","—"); low_a = ins.get("low_airline","—")
        _chart_card("03_price_by_airline",
            "Airline Fares — Median by Carrier · Tier Comparison",
            f"""<strong>Key insights:</strong><br>
            · <strong>{top_a}</strong> charges the highest median fares<br>
            · <strong>{low_a}</strong> is the most affordable carrier<br>
            · Premium carriers charge 30–60% more than budget for identical routes<br>
            · Airline_Tier (3-class grouping) compresses 15 carriers
              into a compact, powerful signal""")

    # ── Distance ───────────────────────────────────────────────────────────────
    with tabs[3]:
        dcorr = ins.get("distance_corr","—")
        lm_p  = ins.get("lastminute_premium","—")
        adv   = ins.get("advance_mean",0); lm = ins.get("lastminute_mean",0)
        _chart_card("04_price_vs_distance",
            "Price vs Distance — Scatter by Class · Distance Bands",
            f"""<strong>Key insights:</strong><br>
            · Pearson r = <strong>{dcorr}</strong> — strongest numeric correlation<br>
            · Non-linear at &gt;10,000 km → log-distance engineered as second feature<br>
            · Class stratification visible: same distance, dramatically different prices<br>
            · Distance is the <strong>#2 most important feature</strong>""")

        _chart_card("05_booking_window",
            "Booking Window — Price vs Lead Time · Volume per Bin",
            f"""<strong>Key insights:</strong><br>
            · Last-minute fares (₹{lm:,}) are
              <strong>{lm_p}×</strong> early-bird (₹{adv:,})<br>
            · Steepest drop: 0–30 days<br>
            · Log(Days_Until) captures this non-linear spike efficiently<br>
            · Is_Last_Minute binary flag isolates the extreme end""")

    # ── Seasonality ────────────────────────────────────────────────────────────
    with tabs[4]:
        pk = ins.get("peak_month","—"); off = ins.get("off_peak_month","—")
        wknd = ins.get("weekend_premium_pct","—")
        _chart_card("06_seasonality",
            "Seasonality — Monthly Average · Day-of-Week Effect",
            f"""<strong>Key insights:</strong><br>
            · <strong>{pk}</strong> = peak month (highest fares)<br>
            · <strong>{off}</strong> = cheapest month to fly<br>
            · Dec–Jan, Mar–May are peak seasons (red bars) — holiday demand<br>
            · Weekend departures cost ~<strong>{wknd}% more</strong> than mid-week<br>
            · Best day to fly: Tuesday or Wednesday""")

        _chart_card("14_price_over_time",
            "Monthly Price Trend — Mean · Median · ±1 Std Dev",
            """<strong>Key insights:</strong><br>
            · Clear seasonal waves over 18-month dataset (Jan 2026 – Jun 2027)<br>
            · Wider std dev in peak months — higher demand = more price dispersion<br>
            · TimeSeriesSplit CV exploits this temporal structure for honest validation""")

    # ── Stops ──────────────────────────────────────────────────────────────────
    with tabs[5]:
        _chart_card("07_stops_analysis",
            "Stops — Median Price · Class × Stops Heatmap",
            """<strong>Key insights:</strong><br>
            · Non-stop flights are often the <strong>most expensive</strong> —
              they serve premium high-demand routes<br>
            · 2-stop routes are cheapest — budget indirect routing<br>
            · Business / First prices barely change with stop count — cabin dominates<br>
            · Stops matter most for Economy travellers seeking savings""")

        _chart_card("13_layover_aircraft",
            "Layover Duration · Aircraft Type vs Price",
            """<strong>Key insights:</strong><br>
            · 9+ hr layovers are cheapest — passengers trade time for price<br>
            · Wide-body aircraft (routes &gt;4,000 km) command a strong premium<br>
            · Aircraft_Type encodes the short-haul / long-haul price tier
              with a single clean binary split""")

    # ── BRD Factors ────────────────────────────────────────────────────────────
    with tabs[6]:
        saf_p  = ins.get("saf_zone2_premium_pct","—")
        rest_p = ins.get("restricted_premium_pct","—")
        _chart_card("08_brd_macrofactors",
            "BRD Phase-2 Macro-Factors — 6-Panel Deep Dive",
            f"""<strong>All 4 BRD features verified in SHAP top-10:</strong><br><br>
            <strong>SAF Zone</strong> — EU mandatory zones add ~<strong>{saf_p}%</strong>
              (EU Regulation 2023/2405, real since Jan 2025)<br>
            <strong>Env Surcharge Tier</strong> — EU ETS carbon permits;
              each tier adds ~1.5%<br>
            <strong>Fleet Age</strong> — older jets burn more fuel;
              +0.4%/yr above 8-year baseline<br>
            <strong>Restricted Airspace</strong> — Delhi/Mumbai → Europe rerouting
              adds ~<strong>{rest_p}%</strong> operating cost<br>
            <strong>Geo Risk Score</strong> — continuous version of airspace flag
              with noise for model nuance<br>
            <strong>Fuel Price Index</strong> — monthly oil price simulation;
              +10 USD/bbl ≈ +3% ticket price""")

    # ── Correlations ───────────────────────────────────────────────────────────
    with tabs[7]:
        _chart_card("09_correlation_heatmap",
            "Feature Correlation Matrix — Pearson r (lower triangle)",
            f"""<strong>Key insights:</strong><br>
            · Distance_km is the strongest numeric predictor (r={ins.get('distance_corr','—')})<br>
            · Days_Until_Departure is negatively correlated — early = cheaper<br>
            · BRD features show small but real positive correlations
              (designed for SHAP detectability)<br>
            · Low inter-feature correlation = no multicollinearity risk for tree models<br>
            · Seat_Availability negative — low availability = demand-driven price spike""")

        _chart_card("16_feature_importance_eda",
            "Pearson Correlation with Price — All Numeric Features",
            """<strong>Key insights:</strong><br>
            · EDA-level linear importance only — tree model captures non-linearity better<br>
            · Distance and Days_Until dominate even here<br>
            · BRD features show non-zero coefficients confirming real signal<br>
            · Negative bars (Seat_Availability, Layover_Hours) are intuitive:
              fuller plane → higher price; longer layover → discount""")

    # ── Advanced ───────────────────────────────────────────────────────────────
    with tabs[8]:
        c1, c2 = st.columns(2)
        with c1:
            _chart_card("10_availability_fuel",
                "Seat Availability &amp; Fuel Index vs Price",
                """<strong>Key insights:</strong><br>
                · Low seat availability (near 0) = demand spike → higher fares<br>
                · Fuel index correlates more on long-haul routes (darker colour)<br>
                · Both modelled as continuous — enables non-linear demand curves""")
        with c2:
            _chart_card("11_source_destination",
                "Median Fare by Source &amp; Destination City",
                """<strong>Key insights:</strong><br>
                · European destinations (London, Frankfurt, Paris) command
                  the highest fares — SAF + long-haul premium<br>
                · Indian domestic-range origins have lower base fares —
                  budget carrier dominance<br>
                · Source × Destination interaction partly captured by
                  Stops × Distance engineering term""")
