"""
pages/p2_eda.py  —  EDA & Insights page
Displays all 16 PNG plots saved by ml_pipeline/src/eda.py,
with auto-generated insights from insights.json shown beside each chart.
"""
import streamlit as st
import os, sys
sys.path.insert(0, os.path.dirname(os.path.dirname(__file__)))

from utils.loader import load_insights, get_report_path, report_exists

# ── Helper to display a plot + insight block ──────────────────────────────────
def _plot_card(plot_key: str, title: str, insight_html: str, wide: bool = False):
    fname  = f"{plot_key}.png" if not plot_key.endswith(".png") else plot_key
    exists = report_exists(fname)
    st.markdown('<div class="card">', unsafe_allow_html=True)
    st.markdown(f"**{title}**")
    if exists:
        st.image(get_report_path(fname), use_container_width=True)
    else:
        st.info(f"Run `python train.py` in ml_pipeline/ to generate this chart.", icon="📊")
    if insight_html:
        st.markdown(f'<div class="insight">{insight_html}</div>', unsafe_allow_html=True)
    st.markdown('</div>', unsafe_allow_html=True)


def render():
    ins = load_insights()

    st.markdown("""<div class="page-hdr">
      <h2>📊 EDA & Visual Insights</h2>
      <p>All plots auto-generated by the ML pipeline · Insights extracted from the data ·
         Charts update automatically after every training run.</p>
    </div>""", unsafe_allow_html=True)

    if not ins:
        st.warning("No insights found. Run `cd ml_pipeline && python train.py` to generate EDA plots.",
                   icon="⚙️")
        return

    # Dataset summary metrics
    st.markdown('<div class="card">', unsafe_allow_html=True)
    c1,c2,c3,c4,c5,c6 = st.columns(6)
    for col, lbl, val in zip([c1,c2,c3,c4,c5,c6],[
        "Total Records","Min Price","Max Price","Mean Price","Median Price","Std Dev"],[
        f"{ins.get('total_rows',0):,}",
        f"₹{ins.get('price_min',0):,}",
        f"₹{ins.get('price_max',0):,}",
        f"₹{ins.get('price_mean',0):,}",
        f"₹{ins.get('price_median',0):,}",
        f"₹{ins.get('price_std',0):,}",
    ]):
        with col: st.metric(lbl, val)
    st.markdown('</div>', unsafe_allow_html=True)

    tabs = st.tabs([
        "💰 Price",
        "💺 Class",
        "✈️ Airlines",
        "📏 Distance",
        "📅 Seasonality",
        "🛑 Stops",
        "🌿 BRD Factors",
        "🔗 Correlations",
        "📈 Advanced",
    ])

    # ── 💰 Price ─────────────────────────────────────────────────────────────
    with tabs[0]:
        _plot_card("01_price_distribution", "Price Distribution (Raw + Log + Boxplot)",
            f"""<b>Key takeaways:</b><br>
            • Price range: ₹{ins.get('price_min',0):,} – ₹{ins.get('price_max',0):,}<br>
            • Mean (₹{ins.get('price_mean',0):,}) > Median (₹{ins.get('price_median',0):,})
              — right-skewed due to premium cabin outliers<br>
            • Log-transform (centre chart) removes the skew and makes the distribution
              near-normal — important for linear baseline models<br>
            • Box plots confirm First and Business classes have the widest IQR,
              reflecting high price variability based on route and airline""")

        _plot_card("15_outlier_analysis", "Outlier Analysis (Z-score + IQR by Class)",
            f"""<b>Key takeaways:</b><br>
            • Rows with z-score &gt; 3 are flagged as outliers —
              these are ultra-long-haul First class flights<br>
            • IQR for First class is significantly wider than Economy,
              indicating more price variability at the premium end<br>
            • Outliers are retained in training — they are real price points,
              not data errors""")

    # ── 💺 Class ─────────────────────────────────────────────────────────────
    with tabs[1]:
        eco   = ins.get("economy_mean",0)
        first = ins.get("first_mean",0)
        mult  = ins.get("first_vs_economy","—")
        _plot_card("02_price_by_class", "Mean Price ± Std Dev + Volume by Class",
            f"""<b>Key takeaways:</b><br>
            • First class (₹{first:,}) costs <b>{mult}× Economy</b> (₹{eco:,}) on average<br>
            • Economy dominates volume (~52%) — matching real-world booking distribution<br>
            • Error bars show First/Business have the highest price variance —
              route and airline matter much more at premium tiers<br>
            • <b>Cabin Class is the #1 price driver</b> with ~28% feature importance in the model""")

        _plot_card("12_class_distance_heatmap", "Price Heatmap — Class × Distance Band",
            """<b>Key takeaways:</b><br>
            • At every distance band, First class commands a large premium over Economy<br>
            • Short-haul Business class often costs more than long-haul Economy —
              showing the dominant effect of cabin over route<br>
            • The interaction feature Class × Distance ranks in the top-3 model features
              because neither alone explains pricing as well as their product""")

    # ── ✈️ Airlines ──────────────────────────────────────────────────────────
    with tabs[2]:
        top_a = ins.get("top_airline","—")
        low_a = ins.get("low_airline","—")
        _plot_card("03_price_by_airline", "Median Fare by Airline + Tier Comparison",
            f"""<b>Key takeaways:</b><br>
            • <b>{top_a}</b> charges the highest median fares —
              premium carrier positioning<br>
            • <b>{low_a}</b> is the most affordable on average<br>
            • Premium carriers charge ~30–60% more than budget carriers for
              equivalent routes and cabin classes<br>
            • Airline tier (premium / mid / budget) is an engineered feature
              that captures this signal more compactly than raw airline encoding""")

    # ── 📏 Distance ───────────────────────────────────────────────────────────
    with tabs[3]:
        dcorr = ins.get("distance_corr", "—")
        _plot_card("04_price_vs_distance", "Price vs Distance + Distance Bands",
            f"""<b>Key takeaways:</b><br>
            • Pearson r = <b>{dcorr}</b> — strong positive linear relationship<br>
            • Clear class stratification: same distance, very different prices by cabin<br>
            • The relationship is non-linear at very long distances (>10,000 km)
              — hence log-distance is engineered as a separate feature<br>
            • Distance is the <b>#2 most important feature</b> in the model""")

        _plot_card("05_booking_window", "Price vs Booking Lead Time",
            f"""<b>Key takeaways:</b><br>
            • Last-minute bookings (1–7 days) cost <b>
              {ins.get('lastminute_premium','—')}× more</b>
              than early-bird bookings (90+ days ahead)<br>
            • The price drop is steepest between 0–30 days<br>
            • Most bookings cluster in the 30–90 day window —
              airlines price competitively here to fill seats<br>
            • Booking window is engineered with log-transform to
              capture the steep non-linear price drop""")

    # ── 📅 Seasonality ────────────────────────────────────────────────────────
    with tabs[4]:
        pk_m  = ins.get("peak_month","—")
        off_m = ins.get("off_peak_month","—")
        wknd  = ins.get("weekend_premium_pct","—")
        _plot_card("06_seasonality", "Price by Month + Day of Week",
            f"""<b>Key takeaways:</b><br>
            • <b>{pk_m}</b> has the highest average fares — summer/holiday peak<br>
            • <b>{off_m}</b> is the cheapest month to fly<br>
            • Dec–Jan and Mar–May are peak periods (red bars) —
              school holidays and festival travel drive demand<br>
            • Weekend departures (Sat/Sun) cost ~<b>{wknd}% more</b> than
              mid-week — Tuesday and Wednesday are the cheapest days to fly""")

        _plot_card("14_price_over_time", "Monthly Price Trend (2026–2027)",
            """<b>Key takeaways:</b><br>
            • Monthly price trend shows clear seasonal waves matching
              the month-level analysis<br>
            • The ±1 std dev band is wider in peak months —
              more price dispersion when demand is high<br>
            • The dataset spans 18 months, giving the TimeSeriesSplit
              CV meaningful temporal variance to validate on""")

    # ── 🛑 Stops ──────────────────────────────────────────────────────────────
    with tabs[5]:
        _plot_card("07_stops_analysis", "Stops Analysis + Class × Stops Heatmap",
            """<b>Key takeaways:</b><br>
            • Non-stop flights are often the <b>most expensive</b> —
              they serve high-demand premium routes (e.g. Delhi–London direct)<br>
            • 2-stop flights are cheapest on average — usually budget/indirect routing<br>
            • The heatmap shows Business and First class barely change
              with stop count — cabin is far more dominant<br>
            • Economy shows the biggest stop-driven price gap,
              so stop count matters most for budget travellers""")

        _plot_card("13_layover_aircraft", "Layover Duration + Aircraft Type",
            """<b>Key takeaways:</b><br>
            • Non-stop flights command a premium even compared to 1–4 hr layovers<br>
            • Flights with 9+ hr layovers are cheapest — passengers pay less
              to tolerate long connection waits<br>
            • Wide-body aircraft (used on routes >4,000 km) are significantly more
              expensive than narrow-body — reflecting long-haul pricing<br>
            • Aircraft type is used as a feature to encode the
              short-haul vs long-haul distinction efficiently""")

    # ── 🌿 BRD Factors ────────────────────────────────────────────────────────
    with tabs[6]:
        saf_p  = ins.get("saf_zone2_premium_pct","—")
        rest_p = ins.get("restricted_premium_pct","—")
        _plot_card("08_brd_macrofactors", "BRD Phase-2 Macro-Factor Deep Dive (6 charts)",
            f"""<b>BRD Phase-2 features — all 4 verified in SHAP top-10:</b><br><br>
            <b>SAF Zone</b> — EU mandatory zones (Zone 2) add ~<b>{saf_p}%</b>
              to ticket prices vs no-mandate routes<br>
            <b>Env Surcharge Tier</b> — EU ETS Tier 3 routes carry the highest
              environmental levy; each tier adds ~1.5%<br>
            <b>Fleet Age</b> — older fleets correlate positively with higher fares
              (+0.4% per year above 8yr baseline) — higher fuel burn = higher opex<br>
            <b>Restricted Airspace</b> — routes requiring rerouting
              (Delhi–London, Mumbai–Paris etc.) cost ~<b>{rest_p}%</b> more<br>
            <b>Geo Risk Score</b> — continuous signal derived from restricted flag +
              beta noise — adds nuance beyond the binary flag<br>
            <b>Fuel Price Index</b> — simulates month-on-month oil price variation;
              each +10 USD/bbl adds ~3% to ticket price""")

    # ── 🔗 Correlations ───────────────────────────────────────────────────────
    with tabs[7]:
        _plot_card("09_correlation_heatmap", "Feature Correlation Matrix (Pearson r)",
            f"""<b>Key takeaways:</b><br>
            • <b>Distance_km</b> has the strongest positive correlation with Price (r={ins.get('distance_corr','—')})<br>
            • <b>Days_Until_Departure</b> has a negative correlation —
              booking earlier → lower price<br>
            • BRD macro-factors (SAF_Zone, Env_Surcharge_Tier) show small
              but real positive correlations — exactly what we designed for SHAP<br>
            • Most features are weakly correlated with each other
              (no multicollinearity), which is good for tree-based models<br>
            • Seat_Availability has a negative correlation — fewer seats
              available → higher demand-driven prices""")

        _plot_card("16_feature_importance_eda", "Pearson Correlation with Price (all numeric features)",
            """<b>Key takeaways:</b><br>
            • This is a simple EDA pass — tree model importance (see Model page)
              captures non-linear effects much better<br>
            • Distance and Days_Until_Departure dominate even in linear correlation<br>
            • BRD features show small but non-zero correlations —
              confirming they carry real signal the model can learn<br>
            • Negative bars (Seat_Availability, Layover_Hours) make intuitive sense:
              fuller planes cost more; longer layovers are cheaper""")

    # ── 📈 Advanced ───────────────────────────────────────────────────────────
    with tabs[8]:
        _plot_card("10_availability_fuel", "Seat Availability & Fuel Index vs Price",
            """<b>Key takeaways:</b><br>
            • When seat availability is low (near 0), prices skew higher —
              demand-driven pricing captured by the Seat_Availability feature<br>
            • Higher fuel price index correlates with higher fares,
              especially on long-haul routes (darker blue = more distance)<br>
            • Both features are modelled as continuous variables, allowing
              the tree-based model to capture non-linear demand curves""")

        _plot_card("11_source_destination", "Median Fare by Source + Destination City",
            """<b>Key takeaways:</b><br>
            • European destination cities (London, Frankfurt, Paris) have the
              highest median fares — SAF mandates + long-haul premium<br>
            • Indian domestic-range source cities (Ahmedabad, Chennai) have
              lower origin fares — reflecting budget carrier dominance<br>
            • Source and Destination are label-encoded as separate features,
              and their interaction (route) is partially captured by
              the Stops × Distance interaction term""")
